namespace fiskaltrust.Middleware.Localization.QueuePT.AcceptanceTest.Scenarios;

public class InvoiceScenarios : AbstractScenarioTests
{
    #region Scenario 1: Transactions with cash payment of > 3000 € net should fail

    [Fact]
    public async Task Scenario1_TransactionsWithCashPaymentGreaterThan3000NetShouldFail()
    {
        var originalReceipt = """
            {
                "cbReceiptReference": "{{$guid}}",
                "cbReceiptMoment": "{{$isoTimestamp}}",
                "ftCashBoxID": "{{cashboxid}}",
                "ftReceiptCase": {{ftReceiptCase}},
                "cbChargeItems": [
                    {
                        "Quantity": 1,
                        "Amount": 3001,
                        "Description": "Test",
                        "VATRate": 23,
                        "ftChargeItemCase": 3
                    }
                ],
                "cbPayItems": [
                    {
                        "Amount": 3001,
                        "Description": "Cash",
                        "ftPayItemCase": 1
                    }
                ],
                "cbUser": "Stefan Kert",
                "cbCustomer": {
                    "CustomerVATId": "123456789"
                }
            }
            """;
        var (request, response) = await ProcessReceiptAsync(originalReceipt, (long) ReceiptCase.InvoiceB2C0x1001.WithCountry("PT"));
        response.ftState.State().Should().Be(State.Error);
        response.ftSignatures[0].Data.Should().Contain("EEEE_Individual cash payment exceeds the legal limit of 3000€. No single cash payment can exceed this amount in Portugal.");
    }

    #endregion
}